const dummySeatBookings = {};

export const liveShowData = (() => {
    const dates = getLiveDates(7);
    const shows = {};
    dates.forEach(date => {
        shows[date] = getLiveShowTimes(date).map((time, idx) => {
            const showId = `show_${date}_${idx}`;
            if (!dummySeatBookings[showId]) {
                dummySeatBookings[showId] = [];
                if (Math.random() > 0.5) {
                    dummySeatBookings[showId].push(seatMap[Math.floor(Math.random() * seatMap.length)]);
                }
            }
            return {
                time,
                showId,
                bookedSeats: dummySeatBookings[showId],
                availableSeats: seatMap.filter(seat => !dummySeatBookings[showId].includes(seat)),
            };

            });
    });
    return shows;
})();

export function getUpcomingShows(date) {
    const now = new Date();
    return (liveShowData[date] || []).filter(show => {
        const showTime = new Date(show.time);
        return showTime > now && (showTime - now) < 24 * 60 * 60 * 1000;
    });
}

export function getAvailableSeats(date, showId) {
    const show = (liveShowData[date] || []).find(s => s.showId === showId);
    return show ? show.availableSeats : [];
}

export function bookSeat(date, showId, seat) {
    if (!dummySeatBookings[showId]) dummySeatBookings[showId] = [];
    if (!dummySeatBookings[showId].includes(seat)) {
        dummySeatBookings[showId].push(seat);
        const show = (liveShowData[date] || []).find(s => s.showId === showId);
        if (show) {
            show.bookedSeats = dummySeatBookings[showId];
            show.availableSeats = seatMap.filter(s => !dummySeatBookings[showId].includes(s));
        }
    }
}


export function getTodayTimeSlots() {
    const slots = [
        { start: "08:00", end: "11:00" },
        { start: "11:30", end: "14:30" },
        { start: "15:00", end: "18:00" },
        { start: "18:30", end: "21:00" },
        { start: "21:30", end: "00:00" }
    ];
    const now = new Date();
    const today = now.toISOString().split('T')[0];
    return slots
        .map(slot => {
            const startDate = new Date(`${today}T${slot.start}:00`);
            const endDate = slot.end === "00:00"
                ? new Date(`${today}T23:59:59`)
                : new Date(`${today}T${slot.end}:00`);
            return {
                time: `${slot.start} - ${slot.end}`,
                startDate,
                endDate
            };
        })
        .filter(slot => now < slot.endDate);

}

